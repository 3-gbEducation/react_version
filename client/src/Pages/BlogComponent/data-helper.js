// Archive
export const archive = (date) => {
  var startDate = date;
  var prevDate;

  let archives = [];

  const addDays = (startDate, days) => {
    var date = new Date(this.valueOf());
    date.setDate(date.getDate() + days);
    return date;
  };

  if (localStorage.archive) {
    if (
      parseInt(startDate.getDate()) === 7 ||
      parseInt(startDate.getDate()) === 14 ||
      parseInt(startDate.getDate()) === 21 ||
      parseInt(startDate.getDate()) === 28
    ) {
      localStorage.removeItem("archive");
      for (let i = 0; i < 5; i++) {
        prevDate = addDays(startDate, -7);
        const ab = { week: i, startDate, prevDate };
        archives.push(ab);
        startDate = prevDate;
      }
      localStorage.setItem("archive", JSON.stringify(archives));
    } else {
      archives = JSON.parse(localStorage.archive);
    }
  } else {
    for (let i = 0; i < 5; i++) {
      prevDate = addDays(startDate, -7);
      const ab = { week: i, startDate, prevDate };
      archives.push(ab);
      startDate = prevDate;
    }
    localStorage.setItem("archive", JSON.stringify(archives));
  }

  return archives;
};

// tags
export const tags = (blogs) => {
  let single = [];
  
  for (let i = 0; i < blogs.length; i++) {
    single = Array.from(new Set([...single, ...blogs[i].tags]));
  }

  let arr = [];

  if (single.length <= 10) {
    arr = single;
  } else {
    while (arr.length < 10) {
      let r = Math.floor(Math.random() * single.length);
      if (arr.indexOf(single[r]) === -1) {
        arr.push(single[r]);
      }
    }
  }
  // console.log(arr);
  return arr;
};

